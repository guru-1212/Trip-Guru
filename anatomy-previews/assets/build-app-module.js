// Generates src/workout/bodyPaths.ts from the downloaded body-highlighter assets.
const fs = require('fs');
const path = require('path');

function loadTs(file, exportName) {
  let src = fs.readFileSync(path.join(__dirname, file), 'utf8');
  src = src.replace(/import[^;]+;/g, '');
  src = src.replace(new RegExp(`export const ${exportName}\\s*:\\s*BodyPart\\[\\]\\s*=`), 'module.exports =');
  const mod = { exports: {} };
  new Function('module', 'exports', src)(mod, mod.exports);
  return mod.exports;
}

const simplify = (arr) =>
  arr.map((p) => ({
    slug: p.slug,
    paths: [...(p.path?.left ?? []), ...(p.path?.right ?? []), ...(p.path?.common ?? [])],
  }));

const front = simplify(loadTs('bodyFront.ts', 'bodyFront'));
const back = simplify(loadTs('bodyBack.ts', 'bodyBack'));

const out = `/**
 * Anatomical body path data (front + back male figure).
 * Artwork from react-native-body-highlighter — MIT License, (c) 2022 ELABBASSI Hicham
 * https://github.com/HichamELBSI/react-native-body-highlighter
 *
 * ViewBoxes: front "0 0 724 1448", back "724 0 724 1448".
 * Generated by anatomy-previews/assets/build-app-module.js — do not edit by hand.
 */

export interface BodyRegion {
  slug: string;
  paths: string[];
}

export const FRONT_VIEWBOX = '0 0 724 1448';
export const BACK_VIEWBOX = '724 0 724 1448';

export const BODY_FRONT: BodyRegion[] = ${JSON.stringify(front, null, 2)};

export const BODY_BACK: BodyRegion[] = ${JSON.stringify(back, null, 2)};
`;

const dest = path.join(__dirname, '..', '..', 'src', 'workout', 'bodyPaths.ts');
fs.writeFileSync(dest, out);
console.log('Wrote', dest, '-', out.length, 'bytes');
